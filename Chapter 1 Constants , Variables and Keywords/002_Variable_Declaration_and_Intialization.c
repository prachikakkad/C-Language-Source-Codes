#include <stdio.h>

int main()
{
    int a;   // Variable declaration
    float b; // Variable declaration
    char p;  // Variable declaration
    a = 23;  // Variable Initialization
    b = 2.3; // Variable Initialization
    p = 'R'; // Variable Initialization
    int d = 16;
    // Variable declaration and initialization together
    float e = 1.6;
    // Variable declaration and initialization together
    char r = 'P';
    // Variable declaration and initialization together

    printf("%d\n" , a);
    printf("%f\n" , b);
    printf("%c\n" , p);
    printf("%d\n" , d);
    printf("%f\n" , e);
    printf("%c\n" , r);
    return 0;
}