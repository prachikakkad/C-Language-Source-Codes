#include <stdio.h>

int main()
{
    for (int i = 1; i < 11; i++) // i++ → ‘i’ incremented / increased by 1.
    {
        printf("%d\n", i);
    }
    printf("\n");
    for (int d = 10; d > 0; d--) // d-- → ‘d’ decremented / decreased by 1.
    {
        printf("%d\n", d);
    }

    return 0;
}